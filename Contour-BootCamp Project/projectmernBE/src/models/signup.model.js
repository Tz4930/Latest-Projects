const mongoose = require("mongoose");

const signupSchema = mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
  }
);
const Signup = mongoose.model('Signup', signupSchema);

module.exports = Signup;

